package alloresto;

public class repas {
		
	 String nom;
	 String ingredients;
	 String asaisonnement;
	 boolean vegan;
	 boolean vegetarien;
	 boolean epice;
	 boolean chaud;
	 
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getIngredients() {
		return ingredients;
	}
	public void setIngredients(String ingredients) {
		this.ingredients = ingredients;
	}
	public String getAsaisonnement() {
		return asaisonnement;
	}
	public void setAsaisonnement(String asaisonnement) {
		this.asaisonnement = asaisonnement;
	}
	public boolean isVegan() {
		return vegan;
	}
	public void setVegan(boolean vegan) {
		this.vegan = vegan;
	}
	public boolean isVegetarien() {
		return vegetarien;
	}
	public void setVegetarien(boolean vegetarien) {
		this.vegetarien = vegetarien;
	}
	public boolean isEpice() {
		return epice;
	}
	public void setEpice(boolean epice) {
		this.epice = epice;
	}
	public boolean isChaud() {
		return chaud;
	}
	public void setChaud(boolean chaud) {
		this.chaud = chaud;
	}
	
	}
	
